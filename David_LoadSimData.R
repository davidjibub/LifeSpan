# ==============================================================================
# 第一步：加载“工具箱” (相当于 MATLAB 的 addpath)
# 必须先运行这些，否则 R 不认识 Load.Subset.Wrapper 是什么
# ==============================================================================
source("100.common-variables.r")
source("101.common-functions.r")
source("300.variables.r")
source("301.functions.r")

# ==============================================================================
# 第二步：加载数据 (相当于 load)
# ==============================================================================

# 1. 设置你要读取的数据标签 (这是 220 脚本生成时默认用的名字)
Tag <- "omega-Wand__.n0000"

# 2. 获取路径配置
PATHS <- Create.Folders(Tag)

# 3. 读取数据到 HOLDER 对象
# LSubset=TRUE 表示只读取用于拟合的子集数据
# LData=TRUE 如果你需要原始完整数据，可以加这个参数
cat("正在从硬盘加载数据...\n")
HOLDER <- Load.Subset.Wrapper(Tag=Tag, LSubset=TRUE, LData=TRUE)

# ==============================================================================
# 第三步：检查加载是否成功
# ==============================================================================
print(dim(HOLDER$SUBSET)) # 看看数据的维度 (行 列)
print(head(HOLDER$SUBSET)) # 看看前几行
cat("数据加载完成！HOLDER 对象已就绪。\n")
# ==============================================================================
# 步骤 2: 提取数据并保存为 CSV
# ==============================================================================

# 提取完整数据
# MATLAB类比: simulated_data = HOLDER.DATA;
simulated_data <- HOLDER$DATA

# 查看一下列名，了解包含什么信息
print(colnames(simulated_data))

# 定义保存路径 (保存到当前工作目录)
csv_filename <- "Simulated_Brain_Data.csv"

# 写入 CSV
# write.csv 是 R 的标准函数
# row.names = FALSE 表示不把行号(1,2,3...)作为第一列写入，这通常是我们想要的
write.csv(simulated_data, file = csv_filename, row.names = FALSE)

cat(paste0("数据导出成功！文件已保存为: ", getwd(), "/", csv_filename, "\n"))

